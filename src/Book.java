public class Book {
   private String title, isbn, genre, author;
   private int year, quantity;

   public Book (String title, String isbn, String genre, String author, int year, int quantity) {
       this.title = title;
       this.isbn = isbn;
       this.genre = genre;
       this.author = author;
       this.year = year;
       this.quantity = quantity;
   }
}
